package mx.com.amx.unotv.noticias.upload.images.dto;

public class BannerDTO {
	
	private String url_imagen;
	private String link;
	private String ad_unit;
	/**
	 * @return the url_imagen
	 */
	public String getUrl_imagen() {
		return url_imagen;
	}
	/**
	 * @param url_imagen the url_imagen to set
	 */
	public void setUrl_imagen(String url_imagen) {
		this.url_imagen = url_imagen;
	}
	/**
	 * @return the link
	 */
	public String getLink() {
		return link;
	}
	/**
	 * @param link the link to set
	 */
	public void setLink(String link) {
		this.link = link;
	}
	/**
	 * @return the ad_unit
	 */
	public String getAd_unit() {
		return ad_unit;
	}
	/**
	 * @param ad_unit the ad_unit to set
	 */
	public void setAd_unit(String ad_unit) {
		this.ad_unit = ad_unit;
	}
	
	
		
}
